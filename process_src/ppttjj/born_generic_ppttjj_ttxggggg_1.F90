  subroutine set_permutation(perm) &
      & bind(c,name="ol_f_set_permutation_ppttjj_ttxggggg_1")
    use ol_parameters_decl_/**/DREALKIND, only: out_symmetry_on
    use ol_generic, only: factorial
    implicit none
    integer, intent(in) :: perm(7)
    integer :: i, j, ii, jj
    integer :: particle_types_perm_ppttjj_ttxggggg_1(7)
    external_perm_ppttjj_ttxggggg_1 = perm
    do i = 1, 7
      external_perm_inv_ppttjj_ttxggggg_1( &
        external_perm_ppttjj_ttxggggg_1(i)) = i
      particle_types_perm_ppttjj_ttxggggg_1(i) = &
        particle_types_ppttjj_ttxggggg_1( &
        external_perm_ppttjj_ttxggggg_1(i))
    end do
    do i = 1, 7
      do j = 1, i
        if (external_perm_ppttjj_ttxggggg_1(i) >= &
          external_perm_ppttjj_ttxggggg_1(j)) then
          ii = external_perm_ppttjj_ttxggggg_1(i)
          jj = external_perm_ppttjj_ttxggggg_1(j)
        else
          ii = external_perm_ppttjj_ttxggggg_1(j)
          jj = external_perm_ppttjj_ttxggggg_1(i)
        end if
        extcomb_perm_ppttjj_ttxggggg_1((i*(i-1))/2 + j) = (ii*(ii-1))/2 + jj
      end do
    end do
    ! Colour and helicity average factor
    average_factor_ppttjj_ttxggggg_1 = &
      average_factors_ppttjj_ttxggggg_1( &
      external_perm_ppttjj_ttxggggg_1(1)) &
      * average_factors_ppttjj_ttxggggg_1( &
      external_perm_ppttjj_ttxggggg_1(2))
    ! Symmetry factor for outgoing particles
    if (out_symmetry_on /= 0) then
      do i = 1, 7
        average_factor_ppttjj_ttxggggg_1 = &
          average_factor_ppttjj_ttxggggg_1 &
          * factorial(count(particle_types_perm_ppttjj_ttxggggg_1(3:7) == i))
      end do
    end if
  end subroutine set_permutation


  subroutine set_permutation_c(perm) &
      & bind(c,name="ol_set_permutation_ppttjj_ttxggggg_1")
    use, intrinsic :: iso_c_binding, only: c_int
    implicit none
    integer(c_int), intent(in) :: perm(7)
    integer :: f_perm(7)
