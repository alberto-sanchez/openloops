#ifdef PRECISION_dp
subroutine amp2(P_scatt, M2, I, MOM, nextcombs, extcombs, M2munu) &
    & bind(c,name="ol_f_amp2_ppjj_ggggg_1")
#else
subroutine amp2(P_scatt, M2, I, MOM, nextcombs, extcombs, M2munu)
  use ol_tree_ppjj_ggggg_1_/**/DREALKIND, only: ntry, nhel, Hel, M1helarr
#endif
! P_scatt(0:3,Npart) = incoming external momenta
! M2  = helicity-summed squared matrix element for glue glue glue glue glue -> 0
! I   = emitter, 0 means none (replace wave function I in the current crossing by the momentum MOM),
!       for I < 0 emitter for PowHeg's B^mu,nu
! MOM = external "polarisation vector" for gluon emitter
! nextcombs is the length of the array extcombs
! The elements of the array extcombs specify for which external particle combinations
!   the colour correlations will be calculated. For particles i,j: i*(i-1)/2+j
!   i=j=0 -> 0 means no colour insertion.
! M2munu = Spin correlated born squared amplitude in PowHeg format B^mu,nu for emitter -I
! **********************************************************************
  use ol_parameters_decl_/**/REALKIND !, only: ci, parameters_status, ZERO, scalefactor, >masses<
  use ol_parameters_init_/**/REALKIND, only: ensure_mp_init
  use ol_kinematics_/**/REALKIND, only: conv_mom_scatt2in, internal_momenta
  use ol_momenta_decl_/**/DREALKIND, only: momenta_nan_check
  use ol_momenta_decl_/**/REALKIND, only: Q
  use ol_data_types_/**/REALKIND
  use ol_helicity_bookkeeping_/**/REALKIND, only: &
    & helbookkeeping_wf, helsync, flip_phase
  use ol_helicity_init, only: helbookkeeping_flip, helsync_flip
  use ol_h_propagators_/**/REALKIND
  use ol_h_wavefunctions_/**/REALKIND
  use ol_h_vertices_/**/REALKIND
  use ol_h_contractions_/**/REALKIND
  use ol_external_ppjj_ggggg_1, only: external_perm_ppjj_ggggg_1, &
    & external_perm_inv_ppjj_ggggg_1, extcomb_perm_ppjj_ggggg_1, &
    & average_factor_ppjj_ggggg_1
  use ol_external_ppjj_ggggg_1, only: H, hel_not_initialised, hel_init
  use ol_colourmatrix_ppjj_ggggg_1_/**/REALKIND, only: colmat_not_initialised, colourmatrix_init
  use ol_forced_parameters_ppjj_ggggg_1_/**/REALKIND, only: check_forced_parameters
  use ol_heltables_ppjj_ggggg_1
  use ol_kinematics_/**/REALKIND, only: LC2Std_Rep_cmplx
  implicit none

  real(DREALKIND), intent(in)  :: P_scatt(0:3,5)
  real(REALKIND),  intent(out) :: M2(0:17-1)
  real(REALKIND),  intent(out) :: M2munu(4,4)
  integer,         intent(in)  :: I
  real(REALKIND),  intent(in)  :: MOM(0:3)
  integer,         intent(in)  :: nextcombs
  integer,         intent(in)  :: extcombs(nextcombs)

  integer           :: ReplacePol, JBmunu, extcombs_permuted(nextcombs), shift, k, r, l, m, n
  real(REALKIND)    :: P(0:3,5)
  real(REALKIND)    :: extmasses2(5)
  real(REALKIND)    :: M2add(0:17-1), M2munuadd
  complex(REALKIND) :: MOM_LC(4), M1(24), M1helarray(24,32)
  type(wfun) :: ep(2)
  complex(REALKIND) :: epStd(1:4,2)
  integer(intkind1) :: hmunu
  real(REALKIND), save :: scalebackfactor, old_scalefactor = 0
  integer(intkind1) :: nsync

  ! type(wfun) :: ex1(h1), ex2(h2), ... ! external wave functions for h<n> helicities
  type(wfun) :: ex1(2), ex2(2), ex3(2), ex4(2), ex5(2)

  ! type(wfun) :: wf<h>(h,n), ... ! n wave functions with h helicity configurations
  type(wfun) :: wf4(4,10), wf8(8,45), wf32(32,45)

  type(polcont) :: A(32,45)
  complex(REALKIND) :: Aj(45)

  complex(REALKIND) :: omega(2) ! phases for helicity correlations

  call ensure_mp_init()

  if (hel_not_initialised) call hel_init()
  if (colmat_not_initialised) call colourmatrix_init()
  if (factors_status /= parameters_status) then
    ! Note: if factors_init would only be called when parameters changed which are relevant for the factors,
    ! a different 'status' would have to be used, because check_forced_parameters should be called after every parameter change.
    call check_forced_parameters()
    call factors_init()
  end if

  if (momenta_nan_check(P_scatt) /= 0) then
    M2 = 0
    return
  end if

  ! Convert 2 -> n-2 PS-point to n -> 0 (so that P(1) + ... + P(n) = 0)
  extmasses2 = [ rZERO2, rZERO2, rZERO2, rZERO2, rZERO2 ]
  call conv_mom_scatt2in(P_scatt, extmasses2, P, external_perm_inv_ppjj_ggggg_1,5)

  ! internal-propagator momenta in light-cone representation
  call internal_momenta(P,5)

  ! denominators
  call born_denominators_init()

  ReplacePol = 0
  JBmunu = 0
  if (I > 0) then
    ReplacePol = external_perm_ppjj_ggggg_1(I)
  else if (I < 0) then
    JBmunu = external_perm_ppjj_ggggg_1(-I)
  end if

  do k = 1, nextcombs
    extcombs_permuted(k) = extcomb_perm_ppjj_ggggg_1(extcombs(k))
  end do

  if (heltables_not_init) call init_heltables()

  ! external WFs
  call wf_V(P(:,1), rZERO, H1, ex1)
  call wf_V(P(:,2), rZERO, H2, ex2)
  call wf_V(P(:,3), rZERO, H3, ex3)
  call wf_V(P(:,4), rZERO, H4, ex4)
  call wf_V(P(:,5), rZERO, H5, ex5)


  if (ntry == 1) then
    shift = 1
    ! call helbookkeeping_flip(H1, 1, shift, eflip, exthel, firstpol)
    ! call helbookkeeping_wf(H1, ex1, shift) ...
    call helbookkeeping_flip(H1, 1, shift, eflip, exthel, firstpol)
    call helbookkeeping_wf(H1, ex1, shift)
    call helbookkeeping_flip(H2, 2, shift, eflip, exthel, firstpol)
    call helbookkeeping_wf(H2, ex2, shift)
    call helbookkeeping_flip(H3, 3, shift, eflip, exthel, firstpol)
    call helbookkeeping_wf(H3, ex3, shift)
    call helbookkeeping_flip(H4, 4, shift, eflip, exthel, firstpol)
    call helbookkeeping_wf(H4, ex4, shift)
    call helbookkeeping_flip(H5, 5, shift, eflip, exthel, firstpol)
    call helbookkeeping_wf(H5, ex5, shift)

  end if

  ! internal WFs
  ! e.g. call vert_VQ_A(ntry, ex3, ex1, wf1, n1, t1) ...
  call vert_UV_W(ntry, ex1, Q(:,1), ex2, Q(:,2), wf4(:,1), n3(:,1), t3x4(:,:,1))
  call vert_GGG_G(ntry, ex3, ex4, ex5, wf8(:,1), n4(:,1), t4x8(:,:,1))
  call vert_GGG_G(ntry, ex4, ex5, ex3, wf8(:,2), n4(:,2), t4x8(:,:,2))
  call vert_GGG_G(ntry, ex5, ex3, ex4, wf8(:,3), n4(:,3), t4x8(:,:,3))
  call vert_UV_W(ntry, ex1, Q(:,1), ex3, Q(:,4), wf4(:,2), n3(:,2), t3x4(:,:,2))
  call vert_GGG_G(ntry, ex2, ex4, ex5, wf8(:,4), n4(:,4), t4x8(:,:,4))
  call vert_GGG_G(ntry, ex4, ex5, ex2, wf8(:,5), n4(:,5), t4x8(:,:,5))
  call vert_GGG_G(ntry, ex5, ex2, ex4, wf8(:,6), n4(:,6), t4x8(:,:,6))
  call vert_UV_W(ntry, ex1, Q(:,1), ex4, Q(:,8), wf4(:,3), n3(:,3), t3x4(:,:,3))
  call vert_GGG_G(ntry, ex2, ex3, ex5, wf8(:,7), n4(:,7), t4x8(:,:,7))
  call vert_GGG_G(ntry, ex3, ex5, ex2, wf8(:,8), n4(:,8), t4x8(:,:,8))
  call vert_GGG_G(ntry, ex5, ex2, ex3, wf8(:,9), n4(:,9), t4x8(:,:,9))
  call vert_UV_W(ntry, ex1, Q(:,1), ex5, Q(:,16), wf4(:,4), n3(:,4), t3x4(:,:,4))
  call vert_GGG_G(ntry, ex2, ex3, ex4, wf8(:,10), n4(:,10), t4x8(:,:,10))
  call vert_GGG_G(ntry, ex3, ex4, ex2, wf8(:,11), n4(:,11), t4x8(:,:,11))
  call vert_GGG_G(ntry, ex4, ex2, ex3, wf8(:,12), n4(:,12), t4x8(:,:,12))
  call vert_UV_W(ntry, ex2, Q(:,2), ex3, Q(:,4), wf4(:,5), n3(:,5), t3x4(:,:,5))
  call vert_GGG_G(ntry, ex1, ex4, ex5, wf8(:,13), n4(:,13), t4x8(:,:,13))
  call vert_GGG_G(ntry, ex4, ex5, ex1, wf8(:,14), n4(:,14), t4x8(:,:,14))
  call vert_GGG_G(ntry, ex5, ex1, ex4, wf8(:,15), n4(:,15), t4x8(:,:,15))
  call vert_UV_W(ntry, ex2, Q(:,2), ex4, Q(:,8), wf4(:,6), n3(:,6), t3x4(:,:,6))
  call vert_GGG_G(ntry, ex1, ex3, ex5, wf8(:,16), n4(:,16), t4x8(:,:,16))
  call vert_GGG_G(ntry, ex3, ex5, ex1, wf8(:,17), n4(:,17), t4x8(:,:,17))
  call vert_GGG_G(ntry, ex5, ex1, ex3, wf8(:,18), n4(:,18), t4x8(:,:,18))
  call vert_UV_W(ntry, ex2, Q(:,2), ex5, Q(:,16), wf4(:,7), n3(:,7), t3x4(:,:,7))
  call vert_GGG_G(ntry, ex1, ex3, ex4, wf8(:,19), n4(:,19), t4x8(:,:,19))
  call vert_GGG_G(ntry, ex3, ex4, ex1, wf8(:,20), n4(:,20), t4x8(:,:,20))
  call vert_GGG_G(ntry, ex4, ex1, ex3, wf8(:,21), n4(:,21), t4x8(:,:,21))
  call vert_UV_W(ntry, ex3, Q(:,4), ex4, Q(:,8), wf4(:,8), n3(:,8), t3x4(:,:,8))
  call vert_GGG_G(ntry, ex1, ex2, ex5, wf8(:,22), n4(:,22), t4x8(:,:,22))
  call vert_GGG_G(ntry, ex2, ex5, ex1, wf8(:,23), n4(:,23), t4x8(:,:,23))
  call vert_GGG_G(ntry, ex5, ex1, ex2, wf8(:,24), n4(:,24), t4x8(:,:,24))
  call vert_UV_W(ntry, ex3, Q(:,4), ex5, Q(:,16), wf4(:,9), n3(:,9), t3x4(:,:,9))
  call vert_GGG_G(ntry, ex1, ex2, ex4, wf8(:,25), n4(:,25), t4x8(:,:,25))
  call vert_GGG_G(ntry, ex2, ex4, ex1, wf8(:,26), n4(:,26), t4x8(:,:,26))
  call vert_GGG_G(ntry, ex4, ex1, ex2, wf8(:,27), n4(:,27), t4x8(:,:,27))
  call vert_UV_W(ntry, ex4, Q(:,8), ex5, Q(:,16), wf4(:,10), n3(:,10), t3x4(:,:,10))
  call vert_GGG_G(ntry, ex1, ex2, ex3, wf8(:,28), n4(:,28), t4x8(:,:,28))
  call vert_GGG_G(ntry, ex2, ex3, ex1, wf8(:,29), n4(:,29), t4x8(:,:,29))
  call vert_GGG_G(ntry, ex3, ex1, ex2, wf8(:,30), n4(:,30), t4x8(:,:,30))
  call vert_UV_W(ntry, wf4(:,1), Q(:,3), ex5, Q(:,16), wf8(:,31), n3(:,11), t3x8(:,:,1))
  call vert_UV_W(ntry, wf4(:,1), Q(:,3), ex4, Q(:,8), wf8(:,32), n3(:,12), t3x8(:,:,2))
  call vert_UV_W(ntry, wf4(:,1), Q(:,3), ex3, Q(:,4), wf8(:,33), n3(:,13), t3x8(:,:,3))
  call vert_UV_W(ntry, wf4(:,2), Q(:,5), ex5, Q(:,16), wf8(:,34), n3(:,14), t3x8(:,:,4))
  call vert_UV_W(ntry, wf4(:,2), Q(:,5), ex4, Q(:,8), wf8(:,35), n3(:,15), t3x8(:,:,5))
  call vert_UV_W(ntry, ex2, Q(:,2), wf4(:,2), Q(:,5), wf8(:,36), n3(:,16), t3x8(:,:,6))
  call vert_UV_W(ntry, wf4(:,3), Q(:,9), ex5, Q(:,16), wf8(:,37), n3(:,17), t3x8(:,:,7))
  call vert_UV_W(ntry, ex4, Q(:,8), wf4(:,4), Q(:,17), wf8(:,38), n3(:,18), t3x8(:,:,8))
  call vert_UV_W(ntry, ex1, Q(:,1), wf4(:,5), Q(:,6), wf8(:,39), n3(:,19), t3x8(:,:,9))
  call vert_UV_W(ntry, ex3, Q(:,4), wf4(:,3), Q(:,9), wf8(:,40), n3(:,20), t3x8(:,:,10))
  call vert_UV_W(ntry, ex2, Q(:,2), wf4(:,3), Q(:,9), wf8(:,41), n3(:,21), t3x8(:,:,11))
  call vert_UV_W(ntry, ex3, Q(:,4), wf4(:,4), Q(:,17), wf8(:,42), n3(:,22), t3x8(:,:,12))
  call vert_UV_W(ntry, ex1, Q(:,1), wf4(:,6), Q(:,10), wf8(:,43), n3(:,23), t3x8(:,:,13))
  call vert_UV_W(ntry, ex2, Q(:,2), wf4(:,4), Q(:,17), wf8(:,44), n3(:,24), t3x8(:,:,14))
  call vert_UV_W(ntry, ex1, Q(:,1), wf4(:,7), Q(:,18), wf8(:,45), n3(:,25), t3x8(:,:,15))


  ! colour-stripped amplitudes
  do nsync = ntry+ntry-1, ntry+1  !  nsync = 1,2  for 1st point and nsync = 3 later
    call diagrams()
    if (nsync == 1) then
      call helsync(nsync, A, nhel, Hel)
      call helsync_flip(nsync, nhel, Hel, eflip, exthel)
    end if
  end do

  do k = 1, nhel
    call colourvector(A, k, M1helarray(:,k))
  end do
  M1helarray(:,nhel+1:) = 0
  M1helarr = M1helarray ! fill cache

  M2 = 0
  if (ReplacePol == 0) then ! no helicity correlation

    do k = 1, nhel
      call colint(M1helarray(:,k), M2add, extcombs_permuted)
      M2 = M2 + M2add
    end do

  else ! helicity correlation

    call flip_phase(P(:,ReplacePol), firstpol(ReplacePol), MOM, omega)
    do k = 1, nhel
      M1 = M1helarray(:,k)
      r = eflip(k, ReplacePol) ! Flip helicity of external particle ReplacePol (gluon emitter).
      if (r <= nhel) then      ! Only add flipped helicity configuration if it doesn't vanish.
        M1 = M1 + omega(exthel(k,ReplacePol)) * M1helarray(:,r)
      end if
      call colint(M1, M2add, extcombs_permuted)
      M2 = M2 + M2add
    end do
    M2 = 0.25_/**/REALKIND * M2

  end if

  if ( JBmunu /= 0 ) then ! Bmunu helicity correlation
    M2munu = 0
  end if

  ! Colour and helicity average and symmetry factor of outgoing particles
  M2add = M2 / average_factor_ppjj_ggggg_1

  do k = 0, 17-1
    M2(k) = M2add(extcomb_perm_ppjj_ggggg_1(k))
  end do

  if (ntry == 1) ntry = 2

  if (scalefactor /= old_scalefactor) then
    scalebackfactor = scalefactor**(2*5-8)
    old_scalefactor = scalefactor
  end if
  M2 = scalebackfactor * M2

  contains

