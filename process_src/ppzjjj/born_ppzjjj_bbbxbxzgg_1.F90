  call vert_GGG_G(ntry, ex6, ex7, wf4(:,1), wf16(:,2), n4(:,2), t4x16(:,:,2))
  call vert_GGG_G(ntry, ex7, wf4(:,1), ex6, wf16(:,3), n4(:,3), t4x16(:,:,3))
  call vert_AZ_Q(gZd,ntry, ex4, ex5, wf6(:,3), n3(:,4), t3x6(:,:,2))
  call prop_A_Q(ntry, wf6(:,3), Q(:,24), MB, 1_intkind1, wf6(:,4), n2(2))
  call vert_QA_V(ntry, ex2, wf6(:,4), wf12(:,2), n3(:,5), t3x12(:,:,2))
  call vert_ZQ_A(gZd,ntry, ex5, ex1, wf6(:,5), n3(:,6), t3x6(:,:,3))
  call vert_QA_V(ntry, ex2, ex3, wf4(:,2), n3(:,7), t3x4(:,:,2))
  call prop_Q_A(ntry, wf6(:,5), Q(:,17), MB, 1_intkind1, wf6(:,6), n2(3))
  call vert_QA_V(ntry, wf6(:,6), ex4, wf12(:,3), n3(:,8), t3x12(:,:,3))
  call vert_GGG_G(ntry, wf4(:,2), ex6, ex7, wf16(:,4), n4(:,4), t4x16(:,:,4))
  call vert_GGG_G(ntry, ex6, ex7, wf4(:,2), wf16(:,5), n4(:,5), t4x16(:,:,5))
  call vert_GGG_G(ntry, ex7, wf4(:,2), ex6, wf16(:,6), n4(:,6), t4x16(:,:,6))
  call vert_QA_V(ntry, ex1, wf6(:,4), wf12(:,4), n3(:,9), t3x12(:,:,4))
  call vert_QA_V(ntry, ex1, ex4, wf4(:,3), n3(:,10), t3x4(:,:,3))
  call vert_QA_V(ntry, wf6(:,2), ex3, wf12(:,5), n3(:,11), t3x12(:,:,5))
