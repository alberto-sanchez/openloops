  call vert_AZ_Q(gZd,ntry, ex3, ex5, wf6(:,7), n3(:,12), t3x6(:,:,4))
  call prop_A_Q(ntry, wf6(:,7), Q(:,20), MB, 1_intkind1, wf6(:,8), n2(4))
