        external_perm_ppllnnjj_vbs_nexnmxemucdxsxg_2(i)) = i
      particle_types_perm_ppllnnjj_vbs_nexnmxemucdxsxg_2(i) = &
        particle_types_ppllnnjj_vbs_nexnmxemucdxsxg_2( &
        external_perm_ppllnnjj_vbs_nexnmxemucdxsxg_2(i))
    end do
    do i = 1, 9
      do j = 1, i
        if (external_perm_ppllnnjj_vbs_nexnmxemucdxsxg_2(i) >= &
          external_perm_ppllnnjj_vbs_nexnmxemucdxsxg_2(j)) then
          ii = external_perm_ppllnnjj_vbs_nexnmxemucdxsxg_2(i)
          jj = external_perm_ppllnnjj_vbs_nexnmxemucdxsxg_2(j)
        else
          ii = external_perm_ppllnnjj_vbs_nexnmxemucdxsxg_2(j)
          jj = external_perm_ppllnnjj_vbs_nexnmxemucdxsxg_2(i)
        end if
        extcomb_perm_ppllnnjj_vbs_nexnmxemucdxsxg_2((i*(i-1))/2 + j) = (ii*(ii-1))/2 + jj
      end do
    end do
    ! Colour and helicity average factor
    average_factor_ppllnnjj_vbs_nexnmxemucdxsxg_2 = 1
    do i = 1, n_scatt
      average_factor_ppllnnjj_vbs_nexnmxemucdxsxg_2 = &
        average_factor_ppllnnjj_vbs_nexnmxemucdxsxg_2 &
        * average_factors_ppllnnjj_vbs_nexnmxemucdxsxg_2( &
        external_perm_ppllnnjj_vbs_nexnmxemucdxsxg_2(i))
    end do
    ! Symmetry factor for outgoing particles
    if (out_symmetry_on /= 0) then
      do i = 1, 9
        average_factor_ppllnnjj_vbs_nexnmxemucdxsxg_2 = &
          average_factor_ppllnnjj_vbs_nexnmxemucdxsxg_2 &
          * factorial(count(particle_types_perm_ppllnnjj_vbs_nexnmxemucdxsxg_2(n_scatt+1:9) == i))
      end do
    end if
  end subroutine set_permutation


  subroutine set_permutation_c(perm) &
