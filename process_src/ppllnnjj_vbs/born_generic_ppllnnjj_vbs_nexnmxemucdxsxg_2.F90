    integer :: i, j, ii, jj
    integer :: particle_types_perm_ppllnnjj_vbs_nexnmxemucdxsxg_2(9)
    external_perm_ppllnnjj_vbs_nexnmxemucdxsxg_2 = perm
    do i = 1, 9
