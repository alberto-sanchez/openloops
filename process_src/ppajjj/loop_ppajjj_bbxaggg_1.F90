! **********************************************************************
subroutine tree_wavefunctions(P, H, M1, M2, POLSEL)
! P(0:3,npart) = 2 -> n-2 external momenta (standard representation)
! H(npart)     = external-particle helicities
! Writes the tree wave functions to 'wf', denominators to 'den'.
! Returns the Born and counterterm colour vectors M1 and M2.
! **********************************************************************
  use ol_parameters_decl_/**/REALKIND ! masses
  use ol_loop_parameters_decl_/**/REALKIND ! counterterms
  use ol_momenta_decl_/**/REALKIND, only: Q
  use ol_wavefunctions_/**/REALKIND
  use ol_propagators_/**/REALKIND
  use ol_vertices_/**/REALKIND
  use ol_counterterms_/**/REALKIND
  implicit none
  real(REALKIND),    intent(in)  :: P(0:3,6)
  integer,           intent(in)  :: H(6)
  integer,           intent(in), optional  :: POLSEL(6)
  complex(REALKIND), intent(out) :: M1(6), M2(11)
  complex(REALKIND) :: A(450)
  ! external WFs
  if (present(POLSEL)) then
    call pol_wf_Q(P(:,1), rMB, H(1), wf(:,0), POLSEL(1))
    call pol_wf_A(P(:,2), rMB, H(2), wf(:,-1), POLSEL(2))
    call pol_wf_V(P(:,3), rZERO, H(3), wf(:,-2), POLSEL(3))
    call pol_wf_V(P(:,4), rZERO, H(4), wf(:,-3), POLSEL(4))
    call pol_wf_V(P(:,5), rZERO, H(5), wf(:,-4), POLSEL(5))
    call pol_wf_V(P(:,6), rZERO, H(6), wf(:,-5), POLSEL(6))

  else
    call pol_wf_Q(P(:,1), rMB, H(1), wf(:,0), 0)
    call pol_wf_A(P(:,2), rMB, H(2), wf(:,-1), 0)
    call pol_wf_V(P(:,3), rZERO, H(3), wf(:,-2), 0)
    call pol_wf_V(P(:,4), rZERO, H(4), wf(:,-3), 0)
    call pol_wf_V(P(:,5), rZERO, H(5), wf(:,-4), 0)
    call pol_wf_V(P(:,6), rZERO, H(6), wf(:,-5), 0)

  end if

  ! internal WFs
  call vert_VQ_A(wf(:,-2),wf(:,0),wf(:,1))
  call vert_GGG_G(wf(:,-3),wf(:,-4),wf(:,-5),wf(:,2))
  call prop_Q_A(wf(:,1),Q(:,5),MB,1_intkind1,wf(:,3))
  call vert_QA_V(wf(:,3),wf(:,-1),wf(:,4))
  call vert_GGG_G(wf(:,-4),wf(:,-5),wf(:,-3),wf(:,5))
  call vert_GGG_G(wf(:,-5),wf(:,-3),wf(:,-4),wf(:,6))
  call vert_AV_Q(wf(:,-1),wf(:,-2),wf(:,7))
  call prop_A_Q(wf(:,7),Q(:,6),MB,1_intkind1,wf(:,8))
  call vert_QA_V(wf(:,0),wf(:,8),wf(:,9))
  call vert_AV_Q(wf(:,-1),wf(:,-3),wf(:,10))
  call vert_UV_W(wf(:,-4),Q(:,16),wf(:,-5),Q(:,32),wf(:,11))
  call prop_A_Q(wf(:,10),Q(:,10),MB,1_intkind1,wf(:,12))
  call vert_QA_V(wf(:,3),wf(:,12),wf(:,13))
  call vert_VQ_A(wf(:,-4),wf(:,3),wf(:,14))
  call vert_AV_Q(wf(:,12),wf(:,-5),wf(:,15))
  call prop_Q_A(wf(:,14),Q(:,21),MB,1_intkind1,wf(:,16))
  call vert_AV_Q(wf(:,12),wf(:,-4),wf(:,17))
