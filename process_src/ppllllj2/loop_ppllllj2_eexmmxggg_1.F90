! **********************************************************************
subroutine tree_wavefunctions(P, H, M1, M2, POLSEL)
! P(0:3,npart) = 2 -> n-2 external momenta (standard representation)
! H(npart)     = external-particle helicities
! Writes the tree wave functions to 'wf', denominators to 'den'.
! Returns the Born and counterterm colour vectors M1 and M2.
! **********************************************************************
  use ol_parameters_decl_/**/REALKIND ! masses
  use ol_loop_parameters_decl_/**/REALKIND ! counterterms
  use ol_momenta_decl_/**/REALKIND, only: Q
  use ol_wavefunctions_/**/REALKIND
  use ol_propagators_/**/REALKIND
  use ol_vertices_/**/REALKIND
  use ol_counterterms_/**/REALKIND
  implicit none
  real(REALKIND),    intent(in)  :: P(0:3,7)
  integer,           intent(in)  :: H(7)
  integer,           intent(in), optional  :: POLSEL(7)
  complex(REALKIND), intent(out) :: M1(0), M2(2)
  complex(REALKIND) :: A(199)
  ! external WFs
  if (present(POLSEL)) then
    call pol_wf_Q(P(:,1), rZERO, H(1), wf(:,0), POLSEL(1))
    call pol_wf_A(P(:,2), rZERO, H(2), wf(:,-1), POLSEL(2))
    call pol_wf_Q(P(:,3), rZERO, H(3), wf(:,-2), POLSEL(3))
    call pol_wf_A(P(:,4), rZERO, H(4), wf(:,-3), POLSEL(4))
    call pol_wf_V(P(:,5), rZERO, H(5), wf(:,-4), POLSEL(5))
    call pol_wf_V(P(:,6), rZERO, H(6), wf(:,-5), POLSEL(6))
    call pol_wf_V(P(:,7), rZERO, H(7), wf(:,-6), POLSEL(7))

  else
    call pol_wf_Q(P(:,1), rZERO, H(1), wf(:,0), 0)
    call pol_wf_A(P(:,2), rZERO, H(2), wf(:,-1), 0)
    call pol_wf_Q(P(:,3), rZERO, H(3), wf(:,-2), 0)
    call pol_wf_A(P(:,4), rZERO, H(4), wf(:,-3), 0)
    call pol_wf_V(P(:,5), rZERO, H(5), wf(:,-4), 0)
    call pol_wf_V(P(:,6), rZERO, H(6), wf(:,-5), 0)
    call pol_wf_V(P(:,7), rZERO, H(7), wf(:,-6), 0)

  end if

  ! internal WFs
  call vert_AQ_S(gH,wf(:,-1),wf(:,0),wf(:,1))
  call vert_AQ_S(gH,wf(:,-3),wf(:,-2),wf(:,2))
  call vert_UV_W(wf(:,-4),Q(:,16),wf(:,-5),Q(:,32),wf(:,3))
  call counter_SSG_G(wf(:,1),wf(:,2),wf(:,-6),wf(:,4))
  call vert_QA_V(wf(:,0),wf(:,-1),wf(:,5))
  call vert_QA_V(wf(:,-2),wf(:,-3),wf(:,6))
  call counter_VVG_G(wf(:,5),wf(:,6),wf(:,-6),wf(:,7))
  call vert_QA_Z(gZl,wf(:,-2),wf(:,-3),wf(:,8))
  call prop_W_W(wf(:,8),Q(:,12),MZ,1_intkind1,wf(:,9))
  call counter_VVG_G(wf(:,5),wf(:,9),wf(:,-6),wf(:,10))
  call vert_QA_Z(gZl,wf(:,0),wf(:,-1),wf(:,11))
  call prop_W_W(wf(:,11),Q(:,3),MZ,1_intkind1,wf(:,12))
  call counter_VVG_G(wf(:,12),wf(:,6),wf(:,-6),wf(:,13))
  call counter_VVG_G(wf(:,12),wf(:,9),wf(:,-6),wf(:,14))
  call vert_UV_W(wf(:,-4),Q(:,16),wf(:,-6),Q(:,64),wf(:,15))
  call counter_SSG_G(wf(:,1),wf(:,2),wf(:,-5),wf(:,16))
  call counter_VVG_G(wf(:,5),wf(:,6),wf(:,-5),wf(:,17))
  call counter_VVG_G(wf(:,5),wf(:,9),wf(:,-5),wf(:,18))
  call counter_VVG_G(wf(:,12),wf(:,6),wf(:,-5),wf(:,19))
  call counter_VVG_G(wf(:,12),wf(:,9),wf(:,-5),wf(:,20))
  call vert_UV_W(wf(:,-5),Q(:,32),wf(:,-6),Q(:,64),wf(:,21))
  call counter_SSG_G(wf(:,1),wf(:,2),wf(:,-4),wf(:,22))
  call counter_VVG_G(wf(:,5),wf(:,6),wf(:,-4),wf(:,23))
  call counter_VVG_G(wf(:,5),wf(:,9),wf(:,-4),wf(:,24))
  call counter_VVG_G(wf(:,12),wf(:,6),wf(:,-4),wf(:,25))
  call counter_VVG_G(wf(:,12),wf(:,9),wf(:,-4),wf(:,26))
  call counter_GGG_V(ctZGGG,wf(:,-4),wf(:,-5),wf(:,-6),wf(:,27))
  call prop_W_W(wf(:,27),Q(:,112),MZ,1_intkind1,wf(:,28))
  call vert_SV_V(wf(:,1),wf(:,9),wf(:,29))
  call counter_GGG_V(ctZGGG,wf(:,-4),wf(:,-6),wf(:,-5),wf(:,30))
  call prop_W_W(wf(:,30),Q(:,112),MZ,1_intkind1,wf(:,31))
  call vert_SV_V(wf(:,2),wf(:,12),wf(:,32))
  call counter_GGG_V(ctAGGG,wf(:,-4),wf(:,-5),wf(:,-6),wf(:,33))
  call vert_QS_A(gH,wf(:,-2),wf(:,1),wf(:,34))
  call vert_AV_Q(wf(:,-3),wf(:,33),wf(:,35))
  call prop_Q_A(wf(:,34),Q(:,7),ZERO,0_intkind1,wf(:,36))
  call counter_GGG_V(ctAGGG,wf(:,-4),wf(:,-6),wf(:,-5),wf(:,37))
  call vert_AV_Q(wf(:,-3),wf(:,37),wf(:,38))
