  call vert_VQ_A(wf(:,-3),wf(:,3),wf(:,24))
  call vert_AV_Q(wf(:,22),wf(:,-5),wf(:,25))
  call prop_Q_A(wf(:,24),Q(:,13),MT,1_intkind1,wf(:,26))
  call vert_AV_Q(wf(:,22),wf(:,-3),wf(:,27))
  call prop_A_Q(wf(:,27),Q(:,26),MT,1_intkind1,wf(:,28))
